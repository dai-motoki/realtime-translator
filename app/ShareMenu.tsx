/**
 * A share button that opens a popover of share targets (copy link + socials).
 * `shareUrl` defaults to the current page; `copyText` defaults to that URL.
 */
export function ShareMenu({
  shareText,
  shareUrl,
  copyText,
  align = "right",
}: {
  shareText: string;
  shareUrl?: string;
  copyText?: string;
  align?: "left" | "right";
}) {
  const tx = useT();
  const [open, setOpen] = useState(false);
  const [copied, setCopied] = useState(false);
  const wrapRef = useRef<HTMLDivElement | null>(null);

  useEffect(() => {
    if (!open) return;
    const onDoc = (e: MouseEvent) => {
      if (wrapRef.current && !wrapRef.current.contains(e.target as Node)) {
        setOpen(false);
      }
    };
    const onKey = (e: KeyboardEvent) => {
      if (e.key === "Escape") setOpen(false);
    };
    document.addEventListener("mousedown", onDoc);
    document.addEventListener("keydown", onKey);
    return () => {
      document.removeEventListener("mousedown", onDoc);
      document.removeEventListener("keydown", onKey);
    };
  }, [open]);

  const url = () =>
    shareUrl ?? (typeof window !== "undefined" ? window.location.href : "");

  const openTarget = (href: string) => {
    window.open(href, "_blank", "noopener,noreferrer,width=620,height=640");
    setOpen(false);
  };

  const copy = async () => {
    const text = copyText ?? url();
    try {
      await navigator.clipboard.writeText(text);
      setCopied(true);
      window.setTimeout(() => setCopied(false), 1400);
    } catch {
      // clipboard unavailable — no-op
    }
  };

  return (
    <div className="share" ref={wrapRef}>
      <button
        type="button"
        className={`share-trigger${open ? " on" : ""}`}
        onClick={() => setOpen((v) => !v)}
        aria-expanded={open}
        aria-label={tx("Share")}
        title={tx("Share")}
      >
        <svg viewBox="0 0 24 24" width="18" height="18" fill="currentColor" aria-hidden>
          <path d="M18 16.08c-.76 0-1.44.3-1.96.77L8.91 12.7c.05-.23.09-.46.09-.7s-.04-.47-.09-.7l7.05-4.11c.54.5 1.25.81 2.04.81 1.66 0 3-1.34 3-3s-1.34-3-3-3-3 1.34-3 3c0 .24.04.47.09.7L8.04 9.81C7.5 9.31 6.79 9 6 9c-1.66 0-3 1.34-3 3s1.34 3 3 3c.79 0 1.5-.31 2.04-.81l7.12 4.16c-.05.21-.08.43-.08.65 0 1.61 1.31 2.92 2.92 2.92s2.92-1.31 2.92-2.92-1.31-2.92-2.92-2.92z" />
        </svg>
      </button>
      {open && (
        <div className={`share-pop ${align}`} role="menu">
          <button
            type="button"
            className="share-btn"
            onClick={copy}
            title={tx("Copy link")}
          >
            {copied ? <span className="share-ok">✓</span> : <LinkIcon />}
          </button>
          {TARGETS.map((t) => (
            <button
              key={t.id}
              type="button"
              className="share-btn"
              onClick={() => openTarget(t.href(url(), shareText))}
              title={t.label}
              aria-label={t.label}
            >
              {t.icon}
            </button>
          ))}
        </div>
      )}
    </div>
  );
}
